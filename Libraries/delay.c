// 返回: none.
//========================================================================
void delay1ms()	//1毫秒@24.000MHz
{
  uint8 i, j;
  _nop_();
  i = 32;
  j = 40;
  do { while (--j);} while (--i);
}

//========================================================================
// 描述: 延迟指定毫秒.
// 参数: 延迟时间（0-65535）.
// 返回: none.
//========================================================================
void delay(uint16 time)
{
  do { delay1ms();} while (--time);
}

